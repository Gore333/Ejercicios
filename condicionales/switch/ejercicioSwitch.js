//ejemplo de como se usa swith 

let color = prompt("Ingrese color")
switch (color){
    case "rojo" : 
      console.log("Color rojo")
      break;
    case "azul" : 
      console.log("Color azul")
      break;
    case "amarillo" : 
      console.log("Color amarillo")
      break; 
    default : 
       console.log("Ingrese dato valido")
       break;
}